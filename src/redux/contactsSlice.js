import { createSlice } from '@reduxjs/toolkit';
import { fetchContacts, addContact, deleteContact } from './operations';

const contactsInitialState = {
  items: [],
  isLoading: false,
  error: null,
};

const pendingStatus = state => {
  state.isLoading = true;
};

const rejectedStatus = (state, action) => {
  state.isLoading = false;
  state.error = action.payload;
};

const contactsSlice = createSlice({
  name: 'contacts',
  initialState: contactsInitialState,
  extraReducers: {
    // =======  PENDING  ==============
    [fetchContacts.pending]: pendingStatus,
    [addContact.pending]: pendingStatus,
    [deleteContact.pending]: pendingStatus,
    // =======  REJECTED  =================
    [fetchContacts.rejected]: rejectedStatus,
    [addContact.rejected]: rejectedStatus,
    [deleteContact.rejected]: rejectedStatus,
    // ========  FULFILLED  ================
    [fetchContacts.fulfilled](state, action) {
      state.isLoading = false;
      state.error = null;
      state.items = action.payload;
    },
    [addContact.fulfilled](state, action) {
      state.isLoading = false;
      state.error = null;
      state.items.push(action.payload);
    },
    [deleteContact.fulfilled](state, action) {
      state.isLoading = false;
      state.error = null;
      const index = state.items.findIndex(
        contact => contact.id === action.payload.id
      );
      state.items.splice(index, 1);
    },
  },
});

export const contactsReducer = contactsSlice.reducer;
